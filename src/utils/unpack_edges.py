
def unpack_edges(tformedEdges):

    edge1A_rc = tformedEdges["edge1A_rc"]
    edge2A_rc = tformedEdges["edge2A_rc"]
    edge1B_rc = tformedEdges["edge1B_rc"]
    edge2B_rc = tformedEdges["edge2B_rc"]
    edge1C_rc = tformedEdges["edge1C_rc"]
    edge2C_rc = tformedEdges["edge2C_rc"]
    edge1D_rc = tformedEdges["edge1D_rc"]
    edge2D_rc = tformedEdges["edge2D_rc"]

    return edge1A_rc, edge2A_rc, edge1B_rc, edge2B_rc, edge1C_rc, edge2C_rc, edge1D_rc, edge2D_rc
